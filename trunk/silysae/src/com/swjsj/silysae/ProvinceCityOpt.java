	public String getUrl(String cityname){
		String param = getParam(cityname);
		if("".equals(param)){
			return "";
		}
		String url = "http://www.weather.gov.cn/publish/forecast/";
		url += param;
		url += "_iframe.html";
		return url;
	}
	
	public String getParam(String cityname){
		List<ProvinceBean> provinceList = PStation.getProvinceList();
		for (int i = 0; i < provinceList.size(); i++) {
			ProvinceBean pb = provinceList.get(i);
			List<StationBean> stationlist = pb.getStationlist();
			for (int j = 0; j < stationlist.size(); j++) {
				StationBean sb = stationlist.get(j);
				if(cityname.equals(sb.getSname())){
					return sb.getPcode() + "/" + sb.getPinyin();
				}
			}
		}
		return "";
	}
