package util;

public class NumberTools {
    public NumberTools() {
    }

    public Long longValue(Number n){
        return n.longValue();
    }

    public Integer intValue(Number n){
        return n.intValue();
    }

    public Short shortValue(Number n){
        return n.shortValue();
    }

    public Byte byteValue(Number n){
        return n.byteValue();
    }


}
